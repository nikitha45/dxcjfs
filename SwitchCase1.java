
public class SwitchCase1 {

		public static void main(String[] args) {

		String alpha="hello"; // 2 byte //int 4 byte 
		
		
		switch (alpha) {
			default: System.out.println("test");
			break;
			case "hello" : System.out.println("hello"); 
			break;
			case "bye" : System.out.println("bye"); 
			break;
			
		}
		
		
	}
}
		